package circlesintersection;

/**
 * Defines what kind of drawing is applied.
 */
public enum DrawKind {
    /**
     * Only dashed circles are drawn.
     */
    circles,
    /**
     * Only intersected arcs are drawn.
     */
    arcs,
    /**
     * Both - dashed circles and intersected arcs.
     */
    both
};
